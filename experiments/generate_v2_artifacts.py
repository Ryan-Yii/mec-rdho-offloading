from __future__ import annotations

import csv
import hashlib
import subprocess
from pathlib import Path

import pandas as pd

from experiments.analyze_results import generate_main_figures
from src.utils.io import ensure_parent, write_rows


ROOT = Path(__file__).resolve().parents[1]
V2 = ROOT / "results" / "v2"
TABLES = ROOT / "paper_tables" / "v2"
FIGURES = ROOT / "figures" / "paper" / "v2"
MANIFEST = ROOT / "paper_artifacts" / "manifest.csv"


def _sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def _commit() -> str:
    return subprocess.check_output(["git", "rev-parse", "HEAD"], cwd=ROOT, text=True).strip()


def _to_markdown(summary: Path, output: Path) -> None:
    ensure_parent(output)
    pd.read_csv(summary).to_markdown(output, index=False)


def _copy_figure_sources() -> list[tuple[str, Path, str, str]]:
    source = V2 / "figures"
    mappings = [
        ("Figure 1", "convergence_curve.png", "Convergence under the V2 reporting objective", "Section 6.2"),
        ("Figure 2", "energy_comparison.png", "Device-side energy comparison", "Section 6.2"),
        ("Figure 3", "delay_comparison.png", "Mean delay comparison", "Section 6.2"),
        ("Figure 4", "aoi_comparison.png", "Average AoI approximation comparison", "Section 6.2"),
        ("Figure 5", "qoe_fairness_comparison.png", "QoE and per-user fairness comparison", "Section 6.2"),
        ("Figure 6", "csr_comparison.png", "Soft QoS CSR comparison", "Section 6.2"),
        ("Figure 7", "radar_chart.png", "Normalised multi-metric illustration", "Section 6.2"),
        ("Figure 8", "ablation_study.png", "One-factor RDHO ablation", "Section 6.3"),
        ("Figure 9", "scalability.png", "Scalability results", "Section 6.4"),
    ]
    copied = []
    FIGURES.mkdir(parents=True, exist_ok=True)
    for item, filename, title, location in mappings:
        candidate = source / filename
        if candidate.exists():
            destination = FIGURES / filename
            destination.write_bytes(candidate.read_bytes())
            copied.append((item, destination, title, location))
    return copied


def _write_execution_report() -> None:
    paths = sorted(V2.rglob("*.csv"))
    lines = ["# V2 Experiment Execution Report", "", "All listed files were generated by V2 scripts from seeded configurations.", ""]
    for path in paths:
        rel = path.relative_to(ROOT)
        rows = max(0, sum(1 for _ in path.open(encoding="utf-8")) - 1)
        lines.append(f"- `{rel}`: {rows} data rows; SHA-256 `{_sha256(path)}`")
    (ROOT / "docs" / "experiment_execution_report.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    main_raw = V2 / "raw" / "main_30_raw_results.csv"
    main_curve = V2 / "raw" / "main_30_convergence.csv"
    generate_main_figures(main_raw, main_curve, V2 / "figures")

    tables = [
        ("Table 1", V2 / "summary" / "main_30_summary_mean_std.csv", "Main paired comparison", "Section 6.2"),
        ("Table 2", V2 / "summary" / "equal_nfe_30_summary_mean_std.csv", "Equal-NFE comparison", "Section 6.2"),
        ("Table 3", V2 / "summary" / "common_control_30_summary_mean_std.csv", "Common-initialisation/post-processing control", "Section 6.3"),
        ("Table 4", V2 / "summary" / "ablation_30_summary_mean_std.csv", "One-factor ablation", "Section 6.3"),
        ("Table 5", V2 / "summary" / "scalability_summary_mean_std.csv", "Scalability", "Section 6.4"),
    ]
    records = []
    for item, source, title, location in tables:
        output = TABLES / f"{source.stem}.md"
        _to_markdown(source, output)
        records.append({
            "manuscript_item": item,
            "paper_number": item.split()[-1],
            "title": title,
            "source_script": "experiments/generate_v2_artifacts.py",
            "source_data": str(source.relative_to(ROOT)),
            "generated_file": str(output.relative_to(ROOT)),
            "file_hash": _sha256(output),
            "last_generated_commit": _commit(),
            "inserted_manuscript_location": location,
        })
    for item, figure, title, location in _copy_figure_sources():
        records.append({
            "manuscript_item": item,
            "paper_number": item.split()[-1],
            "title": title,
            "source_script": "experiments/analyze_results.py",
            "source_data": str(main_raw.relative_to(ROOT)),
            "generated_file": str(figure.relative_to(ROOT)),
            "file_hash": _sha256(figure),
            "last_generated_commit": _commit(),
            "inserted_manuscript_location": location,
        })
    write_rows(MANIFEST, records)
    _write_execution_report()


if __name__ == "__main__":
    main()
